    private void klikTabel() {
        int baris = jTable2.getSelectedRow();

        jTextField1.setText(jTable2.getValueAt(baris, 0).toString());
        jTextField2.setText(jTable2.getValueAt(baris, 1).toString());
        jTextField3.setText(jTable2.getValueAt(baris, 2).toString());

        try {
            java.util.Date tanggal = new SimpleDateFormat("yyyy-MM-dd")
                    .parse(jTable2.getValueAt(baris, 3).toString());
            jDateChooser1.setDate(tanggal);
        } catch (Exception e) {
            // Ignored
        }

        jTextField4.setText(jTable2.getValueAt(baris, 4).toString());
    }
