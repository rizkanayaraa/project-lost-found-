    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        try {
            String tanggal = new SimpleDateFormat("yyyy-MM-dd").format(jDateChooser1.getDate());
            conn = CampusLostFound.koneksiDB();

            String sql = "UPDATE klaim_barang SET id_barang_temuan=?, id_user=?, tanggal_klaim=?, status_verifikasi=? WHERE id_klaim=?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, Integer.parseInt(jTextField2.getText()));
            ps.setInt(2, Integer.parseInt(jTextField3.getText()));
            ps.setString(3, tanggal);
            ps.setString(4, jTextField4.getText());
            ps.setInt(5, Integer.parseInt(jTextField1.getText()));

            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
            tampilData();
            clearForm();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }                        
    }                                        

