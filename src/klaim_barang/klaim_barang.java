    private void tampilData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Klaim");
        model.addColumn("ID Barang Temuan");
        model.addColumn("ID User");
        model.addColumn("Tanggal Klaim");
        model.addColumn("Status Verifikasi");

        try {
            String sql = "SELECT * FROM klaim_barang";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_klaim"),
                    rs.getString("id_barang_temuan"),
                    rs.getString("id_user"),
                    rs.getDate("tanggal_klaim"),
                    rs.getString("status_verifikasi")
                });
            }
            jTable2.setModel(model); // Menyesuaikan ke tabel aktif kamu (jTable2)
            
            // ===== PENGATUR TAMPILAN TABEL AGAR RAPI =====
            jTable2.setRowHeight(25); // Mengatur tinggi baris agar tidak dempet
            jTable2.getColumnModel().getColumn(0).setPreferredWidth(80);   // ID Klaim
            jTable2.getColumnModel().getColumn(1).setPreferredWidth(120);  // ID Barang Temuan
            jTable2.getColumnModel().getColumn(2).setPreferredWidth(80);   // ID User
            jTable2.getColumnModel().getColumn(3).setPreferredWidth(120);  // Tanggal Klaim
            jTable2.getColumnModel().getColumn(4).setPreferredWidth(150);  // Status Verifikasi
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
        }
    }

