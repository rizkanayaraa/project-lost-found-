    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
            try {
            // 1. Validasi apakah user sudah memilih data di tabel atau belum
            if (idSiswa == null || idSiswa.equals("")) {
                JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus terlebih dahulu melalui tabel");
                return;
            }

            // 2. Memunculkan dialog konfirmasi (Yes/No)
            int konfirmasi = JOptionPane.showConfirmDialog(
                    this,
                    "Yakin ingin menghapus data ini?",
                    "Konfirmasi Hapus",
                    JOptionPane.YES_NO_OPTION);

            // 3. Jika user menekan tombol YES, maka proses hapus dijalankan
            if (konfirmasi == JOptionPane.YES_OPTION) {

                // Memanggil class koneksi milikmu
                conn = CampusLostFound.koneksiDB(); 

                // Query disesuaikan dengan nama tabel 'user' dan primary key 'id_user' kamu
                String sql = "DELETE FROM user WHERE id_user=?"; 

                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, idSiswa); // Menggunakan variabel idSiswa yang didapat dari mouseClicked tadi

                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus");

                // 4. Refresh tabel agar data yang dihapus langsung hilang dari layar
                tampilData();

                // 5. Kosongkan kembali form input menggunakan fungsi bersihkanForm() yang sudah kamu punya
                bersihkanForm();

                // Kembalikan variabel idSiswa menjadi kosong
                idSiswa = "";
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data gagal dihapus: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

