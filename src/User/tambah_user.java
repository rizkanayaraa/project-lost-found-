    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String nama = jTextField1.getText();
        String nim = jTextField2.getText();
        String email = jTextField3.getText();
        String no_hp = jTextField4.getText();
        
        // Validasi inputan kosong
        if (nama.equals("") || nim.equals("") || email.equals("") || no_hp.equals("")) {
            JOptionPane.showMessageDialog(this, "Semua data wajib diisi, ya!");
            return;
        }
        
        try {
            conn = CampusLostFound.koneksiDB();
            String sql = "INSERT INTO user (nama, nim, email, no_hp) VALUES (?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            
            pst.setString(1, nama);
            pst.setString(2, nim);
            pst.setString(3, email);
            pst.setString(4, no_hp);
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data User Berhasil Disimpan!");
            
            tampilData();     // Memperbarui tabel jTable2 secara otomatis
            bersihkanForm();  // Mengosongkan form teks kembali
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Menyimpan: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
            try {
            // 1. Validasi apakah user sudah memilih data yang akan diedit dari tabel
            if (idSiswa == null || idSiswa.equals("")) {
                JOptionPane.showMessageDialog(this, "Pilih data yang akan diedit terlebih dahulu melalui tabel");
                return;
            }

            // Memanggil class koneksi sesuai struktur project-mu
            conn = CampusLostFound.koneksiDB();

            // 2. Query UPDATE disesuaikan dengan nama tabel 'user' dan kolom-kolommu
            String sql = "UPDATE user SET "
                    + "nama=?, "
                    + "nim=?, "
                    + "email=?, "
                    + "no_hp=? "
                    + "WHERE id_user=?";

            PreparedStatement pst = conn.prepareStatement(sql);

            // 3. Mengambil data terbaru dari form input untuk dikirim ke database
            pst.setString(1, jTextField1.getText()); // Mengisi nama (tanda tanya ke-1)
            pst.setString(2, jTextField2.getText()); // Mengisi nim (tanda tanya ke-2)
            pst.setString(3, jTextField3.getText()); // Mengisi email (tanda tanya ke-3)
            pst.setString(4, jTextField4.getText()); // Mengisi no_hp (tanda tanya ke-4)
            pst.setString(5, idSiswa);               // Mengisi id_user sebagai kondisi WHERE (tanda tanya ke-5)

            // 4. Eksekusi query ke database
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil diubah");

            // 5. Refresh tabel agar perubahan langsung terlihat
            tampilData();

            // 6. Bersihkan kembali form inputan
            bersihkanForm();

            // Kosongkan kembali variabel idSiswa
            idSiswa = "";

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data gagal diubah : " + e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

