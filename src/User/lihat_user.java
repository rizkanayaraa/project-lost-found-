private void tampilData(String keyword) {
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        model.addColumn("Nama");
        model.addColumn("NIM");
        model.addColumn("Email");
        model.addColumn("No HP");
        
        try {
            conn = CampusLostFound.koneksiDB();
            String sql;
            
            // Perbaikan Query: Sekarang support pencarian Nama, NIM, Email, dan No HP sekaligus
            if (keyword.equals("")) {
                sql = "SELECT * FROM user";
            } else {
                sql = "SELECT * FROM user WHERE nama LIKE '%" + keyword + "%' "
                    + "OR nim LIKE '%" + keyword + "%' "
                    + "OR email LIKE '%" + keyword + "%' "
                    + "OR no_hp LIKE '%" + keyword + "%'";
            }
            
            Statement stmt = conn.createStatement();
            ResultSet res = stmt.executeQuery(sql);
            
            while (res.next()) {
                model.addRow(new Object[]{
                    res.getString("nama"),
                    res.getString("nim"),
                    res.getString("email"),
                    res.getString("no_hp")
                });
            }
            // Sesuai nama variabel komponen kamu di NetBeans
            jTable1.setModel(model);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
        }
    }

