    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int baris = jTable1.getSelectedRow();
        if (baris == -1) return;

        // Ambil data string dari baris yang diklik untuk modal input / pencarian target
        String namaLama = jTable1.getValueAt(baris, 0).toString();
        String nimLama  = jTable1.getValueAt(baris, 1).toString();
        String emailLama = jTable1.getValueAt(baris, 2).toString();
        String noHpLama  = jTable1.getValueAt(baris, 3).toString();

        // Menyediakan opsi tindakan kepada user dalam bentuk tombol pop-up dialog
        Object[] opsiMenu = {"Edit Data", "Hapus Data", "Batal"};
        int pilihan = JOptionPane.showOptionDialog(this, 
                "Pilih aksi untuk data user: " + namaLama, 
                "Opsi Pintasan Data", 
                JOptionPane.DEFAULT_OPTION, 
                JOptionPane.QUESTION_MESSAGE, 
                null, opsiMenu, opsiMenu[0]);

        // JIKA USER MEMILIH: EDIT DATA
        // JIKA USER MEMILIH: EDIT DATA
        if (pilihan == 0) {
            // 1. Munculkan pop-up input berturut-turut untuk semua data
            String namaBaru = JOptionPane.showInputDialog(this, "Ubah Nama:", namaLama);
            if (namaBaru == null || namaBaru.trim().equals("")) return; 
            
            String nimBaru = JOptionPane.showInputDialog(this, "Ubah NIM:", nimLama);
            if (nimBaru == null || nimBaru.trim().equals("")) return;
            
            String emailBaru = JOptionPane.showInputDialog(this, "Ubah Email:", emailLama);
            if (emailBaru == null || emailBaru.trim().equals("")) return;
            
            String noHpBaru = JOptionPane.showInputDialog(this, "Ubah No HP:", noHpLama);
            if (noHpBaru == null || noHpBaru.trim().equals("")) return;

            try {
                conn = CampusLostFound.koneksiDB();
                
                // 2. Ambil id_user asli dari database berdasarkan NIM sebelum diedit
                String sqlGetId = "SELECT id_user FROM user WHERE nim=?";
                PreparedStatement pstId = conn.prepareStatement(sqlGetId);
                pstId.setString(1, nimLama);
                ResultSet rsId = pstId.executeQuery();
                
                if (rsId.next()) {
                    String idTarget = rsId.getString("id_user");
                    
                    // 3. Query UPDATE diubah agar memperbarui SEMUA KOLOM sekaligus
                    String sqlUpdate = "UPDATE user SET nama=?, nim=?, email=?, no_hp=? WHERE id_user=?";
                    PreparedStatement pstUpdate = conn.prepareStatement(sqlUpdate);
                    pstUpdate.setString(1, namaBaru);
                    pstUpdate.setString(2, nimBaru);
                    pstUpdate.setString(3, emailBaru);
                    pstUpdate.setString(4, noHpBaru);
                    pstUpdate.setString(5, idTarget); // id_user sebagai kunci target
                    
                    pstUpdate.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Semua data berhasil diperbarui!");
                    tampilData(""); // Refresh isi tabel
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal mengedit data: " + e.getMessage());
            }

        // JIKA USER MEMILIH: HAPUS DATA
        } else if (pilihan == 1) {
            int konfirmasi = JOptionPane.showConfirmDialog(this, 
                    "Yakin ingin menghapus data " + namaLama + "?", 
                    "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
            
            if (konfirmasi == JOptionPane.YES_OPTION) {
                try {
                    conn = CampusLostFound.koneksiDB();
                    
                    // Cari id_user asli di database berdasarkan NIM lama yang diklik
                    String sqlGetId = "SELECT id_user FROM user WHERE nim=?";
                    PreparedStatement pstId = conn.prepareStatement(sqlGetId);
                    pstId.setString(1, nimLama);
                    ResultSet rsId = pstId.executeQuery();
                    
                    if (rsId.next()) {
                        String idTarget = rsId.getString("id_user");
                        
                        // Jalankan query DELETE berdasarkan id_user target
                        String sqlDelete = "DELETE FROM user WHERE id_user=?";
                        PreparedStatement pstDelete = conn.prepareStatement(sqlDelete);
                        pstDelete.setString(1, idTarget);
                        
                        pstDelete.executeUpdate();
                        JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus!");
                        tampilData(""); // Refresh isi tabel
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Gagal menghapus: " + e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

