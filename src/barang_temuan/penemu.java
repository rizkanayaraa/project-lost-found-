    private void loadDataTabel() {
        // Membuat judul kolom pada tabel
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
        model.addColumn("Nama Penemu");
        model.addColumn("Nama Barang");
        model.addColumn("Lokasi Temuan");
        model.addColumn("Tanggal");
        model.addColumn("Status");

        try {
            // Memanggil koneksi database
            java.sql.Connection conn = CampusLostFound.koneksiDB();
            java.sql.Statement stm = conn.createStatement();
            
            // Mengambil semua data dari tabel barang_temuan
            java.sql.ResultSet res = stm.executeQuery("SELECT * FROM barang_temuan");

            // Memasukkan data dari database ke dalam baris tabel UI
            while (res.next()) {
                model.addRow(new Object[]{
                    res.getString("nama_penemu"),
                    res.getString("nama_barang"),
                    res.getString("lokasi_temuan"),
                    res.getString("tanggal_temuan"),
                    res.getString("status_pengambilan")
                });
            }
            
            // Set model ke JTable
            jTable1.setModel(model);
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal memuat data tabel: " + e.getMessage());
        }
    }

