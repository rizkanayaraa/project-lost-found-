    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       try {
            // Panggil koneksi database
            java.sql.Connection kon = CampusLostFound.koneksiDB();
            
            // Query harus sesuai urutan: nama_penemu, nama_barang, lokasi_temuan, tanggal_temuan, status_pengambilan
            String sql = "INSERT INTO barang_temuan (nama_penemu, nama_barang, lokasi_temuan, tanggal_temuan, status_pengambilan) VALUES (?, ?, ?, ?, ?)";
            java.sql.PreparedStatement pst = kon.prepareStatement(sql);
            
            // 1. Nama Penemu
            pst.setString(1, txtNama.getText());
            // 2. Nama Barang
            pst.setString(2, txtNamaBarang.getText());
            // 3. Lokasi Ditemukan
            pst.setString(3, txtLokasi.getText());
            
            // 4. Tanggal Ditemukan
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
            String tgl = "";
            if (jDateChooser1.getDate() != null) {
                tgl = sdf.format(jDateChooser1.getDate());
            }
            pst.setString(4, tgl);
            
            // 5. Status Pengambilan
            pst.setString(5, cbStatus.getSelectedItem().toString());
            
            // Validasi: Pastikan user sudah memilih status (bukan "Pilih Status...")
            if (cbStatus.getSelectedIndex() == 0) {
                javax.swing.JOptionPane.showMessageDialog(null, "Tolong pilih status pengembalian terlebih dahulu!");
                return;
            }
            
            // Eksekusi query
            pst.execute();
            javax.swing.JOptionPane.showMessageDialog(null, "Laporan barang temuan berhasil disimpan!");
            
            // Refresh tabel
            loadDataTabel();
            
            // Bersihkan kotak isian
            txtNama.setText("");
            txtNamaBarang.setText("");
            txtLokasi.setText("");
            jDateChooser1.setDate(null);
            cbStatus.setSelectedIndex(0); 
            
        } catch (java.awt.HeadlessException | java.sql.SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Gagal menyimpan data: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
  int baris = jTable1.rowAtPoint(evt.getPoint());
        
        if (baris >= 0) {
            // Menarik data dari tabel ke kotak isian
            txtNama.setText(jTable1.getValueAt(baris, 0) != null ? jTable1.getValueAt(baris, 0).toString() : "");
            txtNamaBarang.setText(jTable1.getValueAt(baris, 1) != null ? jTable1.getValueAt(baris, 1).toString() : "");
            txtLokasi.setText(jTable1.getValueAt(baris, 2) != null ? jTable1.getValueAt(baris, 2).toString() : "");
            
            try {
                String tanggalTabel = jTable1.getValueAt(baris, 3).toString(); 
                java.util.Date date = new java.text.SimpleDateFormat("yyyy-MM-dd").parse(tanggalTabel);
                jDateChooser1.setDate(date);
            } catch (Exception e) {
                jDateChooser1.setDate(null);
                
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    try {
            java.sql.Connection conn = CampusLostFound.koneksiDB(); 
            
            // Perhatikan urutan SET-nya: barang, lokasi, tanggal, status. 
            // WHERE-nya berdasarkan nama_penemu.
            String sql = "UPDATE barang_temuan SET nama_barang = ?, lokasi_temuan = ?, tanggal_temuan = ?, status_pengambilan = ? WHERE nama_penemu = ?";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            
            // Format Tanggal
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
            String tgl = "";
            if (jDateChooser1.getDate() != null) {
                tgl = sdf.format(jDateChooser1.getDate());
            }
            
            // 1. Nama Barang
            pst.setString(1, txtNamaBarang.getText());
            // 2. Lokasi Temuan
            pst.setString(2, txtLokasi.getText());
            // 3. Tanggal Temuan
            pst.setString(3, tgl); 
            // 4. Status Pengambilan
            pst.setString(4, cbStatus.getSelectedItem().toString());
            // 5. Patokan (Nama Penemu)
            pst.setString(5, txtNama.getText());
            
            int teredit = pst.executeUpdate(); 
            
            if (teredit > 0) {
                javax.swing.JOptionPane.showMessageDialog(null, "Data Berhasil Diedit");
                loadDataTabel();  
                txtNama.setText("");     
                txtNamaBarang.setText("");
                txtLokasi.setText("");
                jDateChooser1.setDate(null);
                cbStatus.setSelectedIndex(0); // Reset ke "Pilih Status..."
            } else {
                javax.swing.JOptionPane.showMessageDialog(null, "Gagal Edit! Pastikan Anda mengklik baris di tabel terlebih dahulu.");
            }
            
        } catch (java.awt.HeadlessException | java.sql.SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Perubahan Data Gagal: " + e.getMessage());
        }
    
    }//GEN-LAST:event_jButton3ActionPerformed

