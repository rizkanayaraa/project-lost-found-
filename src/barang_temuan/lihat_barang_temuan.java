    // Fungsi untuk memuat data dari database ke tabel UI
        private void loadDataTabel() {
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Nama Penemu");
            model.addColumn("Nama Barang");
            model.addColumn("Lokasi Temuan");
            model.addColumn("Tanggal");
            model.addColumn("Status");

            try {
                Connection conn = CampusLostFound.koneksiDB();
                String sql = "SELECT * FROM barang_temuan";
                Statement stm = conn.createStatement();
                ResultSet res = stm.executeQuery(sql);

                while (res.next()) {
                    model.addRow(new Object[]{
                        res.getString("nama_penemu"),
                        res.getString("nama_barang"),
                        res.getString("lokasi_temuan"),
                        res.getString("tanggal_temuan"),
                        res.getString("status_pengambilan")
                    });
                }
                jTable1.setModel(model);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage());
            }
        }

        // Fungsi Pencarian Multi-Indikasi (Global Search)
        private void cariData(String keyword) {
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Nama Penemu");
            model.addColumn("Nama Barang");
            model.addColumn("Lokasi Temuan");
            model.addColumn("Tanggal");
            model.addColumn("Status");

            try {
                Connection conn = CampusLostFound.koneksiDB();
                // Mencari kecocokan kata di semua kolom teks utama
                String sql = "SELECT * FROM barang_temuan WHERE nama_penemu LIKE ? "
                           + "OR nama_barang LIKE ? OR lokasi_temuan LIKE ? OR status_pengambilan LIKE ?";

                PreparedStatement pst = conn.prepareStatement(sql);
                String queryKeyword = "%" + keyword + "%";
                pst.setString(1, queryKeyword);
                pst.setString(2, queryKeyword);
                pst.setString(3, queryKeyword);
                pst.setString(4, queryKeyword);

                ResultSet res = pst.executeQuery();
                while (res.next()) {
                    model.addRow(new Object[]{
                        res.getString("nama_penemu"),
                        res.getString("nama_barang"),
                        res.getString("lokasi_temuan"),
                        res.getString("tanggal_temuan"),
                        res.getString("status_pengambilan")
                    });
                }
                jTable1.setModel(model);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Pencarian Gagal: " + e.getMessage());
            }
        }

