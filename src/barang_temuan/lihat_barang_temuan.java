    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int baris = jTable1.getSelectedRow();
        if (baris >= 0) {
            // Ambil data lama sebagai acuan WHERE clause agar tidak salah update data orang lain
            String lamaPenemu = jTable1.getValueAt(baris, 0).toString();
            String lamaBarang = jTable1.getValueAt(baris, 1).toString();
            String lamaLokasi = jTable1.getValueAt(baris, 2).toString();
            String lamaTanggal = jTable1.getValueAt(baris, 3).toString();
            String lamaStatus = jTable1.getValueAt(baris, 4).toString();

            // Pilihan aksi untuk user
            Object[] options = {"Simpan Perubahan (Edit)", "Hapus Baris Ini", "Batal"};
            int n = JOptionPane.showOptionDialog(this,
                "Pilih tindakan untuk data milik: " + lamaPenemu,
                "Kelola Data Barang Temuan",
                JOptionPane.YES_NO_CANCEL_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null, options, options[2]);

            if (n == JOptionPane.YES_OPTION) {
                // --- PROSES EDIT (Bisa edit semua field termasuk nama) ---
                String baruPenemu = JOptionPane.showInputDialog(this, "Ubah Nama Penemu:", lamaPenemu);
                if (baruPenemu == null) return; 
                
                String baruBarang = JOptionPane.showInputDialog(this, "Ubah Nama Barang:", lamaBarang);
                if (baruBarang == null) return;
                
                String baruLokasi = JOptionPane.showInputDialog(this, "Ubah Lokasi Temuan:", lamaLokasi);
                if (baruLokasi == null) return;
                
                String baruTanggal = JOptionPane.showInputDialog(this, "Ubah Tanggal (YYYY-MM-DD):", lamaTanggal);
                if (baruTanggal == null) return;

                String baruStatus = JOptionPane.showInputDialog(this, "Ubah Status Pengembalian:", lamaStatus);
                if (baruStatus == null) return;

                try {
                    Connection conn = CampusLostFound.koneksiDB();
                    // Menggunakan semua parameter data lama di WHERE clause sebagai pengganti ID
                    String sql = "UPDATE barang_temuan SET nama_penemu=?, nama_barang=?, lokasi_temuan=?, tanggal_temuan=?, status_pengambilan=? "
                               + "WHERE nama_penemu=? AND nama_barang=? AND lokasi_temuan=? AND tanggal_temuan=? AND status_pengambilan=?";
                    
                    PreparedStatement pst = conn.prepareStatement(sql);
                    // Data Baru (SET)
                    pst.setString(1, baruPenemu);
                    pst.setString(2, baruBarang);
                    pst.setString(3, baruLokasi);
                    pst.setString(4, baruTanggal);
                    pst.setString(5, baruStatus);
                    // Data Lama (WHERE)
                    pst.setString(6, lamaPenemu);
                    pst.setString(7, lamaBarang);
                    pst.setString(8, lamaLokasi);
                    pst.setString(9, lamaTanggal);
                    pst.setString(10, lamaStatus);

                    pst.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Data Berhasil Diedit!");
                    loadDataTabel(); // Refresh tabel
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Gagal Edit: " + e.getMessage());
                }

            } else if (n == JOptionPane.NO_OPTION) {
                // --- PROSES HAPUS ---
                int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
                if (konfirmasi == JOptionPane.YES_OPTION) {
                    try {
                        Connection conn = CampusLostFound.koneksiDB();
                        String sql = "DELETE FROM barang_temuan WHERE nama_penemu=? AND nama_barang=? AND lokasi_temuan=? AND tanggal_temuan=? AND status_pengambilan=?";
                        PreparedStatement pst = conn.prepareStatement(sql);
                        pst.setString(1, lamaPenemu);
                        pst.setString(2, lamaBarang);
                        pst.setString(3, lamaLokasi);
                        pst.setString(4, lamaTanggal);
                        pst.setString(5, lamaStatus);
                        
                        pst.executeUpdate();
                        JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus!");
                        loadDataTabel(); // Refresh tabel
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, "Gagal Hapus: " + e.getMessage());
                    }
                }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

